"""GitHub integration for the Build factory.

Materialises a build's generated files into a real GitHub repository in a single
commit via the Git Data API (blobs → tree → commit → ref). No local git binary
or persistent workdir needed — the backend stays stateless. Pushing the scaffold's
``.github/workflows/ci.yml`` automatically triggers the CI quality gate.
"""

from __future__ import annotations

import base64
import io
import logging
import zipfile
from typing import Any
from uuid import UUID

import httpx

from app.core.config import get_settings
from app.models.build import Build, BuildStatus, GeneratedFile

logger = logging.getLogger(__name__)

_API = "https://api.github.com"
_GIT_MODE_FILE = "100644"


class GitHubError(RuntimeError):
    pass


def _db_creds() -> tuple[str | None, str | None]:
    """Resolve (token, owner) — DB org config (Admin → AI config) wins over .env."""
    settings = get_settings()
    token = settings.github_repo_token
    owner = settings.github_owner
    try:
        from app.core.database import SyncSessionLocal  # noqa: PLC0415
        from app.models.organization import Organization  # noqa: PLC0415

        from app.core.crypto import decrypt_secret  # noqa: PLC0415

        with SyncSessionLocal() as db:
            org = db.query(Organization).first()
            cfg = (org.ai_provider_configs or {}).get("github_repo") if org else None
            if cfg:
                token = decrypt_secret(cfg.get("token")) or token
                owner = cfg.get("owner") or owner
    except Exception:  # noqa: BLE001 — fall back to env on any DB issue
        pass
    return token, owner


def _token() -> str:
    token, _ = _db_creds()
    if not token:
        raise GitHubError(
            "GitHub is not configured. Add a token in Admin → AI config (GitHub), "
            "or set GITHUB_REPO_TOKEN in .env (a fine-grained PAT with repo create "
            "+ contents + workflow scopes)."
        )
    return token


def _headers() -> dict[str, str]:
    return {
        "Authorization": f"Bearer {_token()}",
        "Accept": "application/vnd.github+json",
        "X-GitHub-Api-Version": "2022-11-28",
    }


def _repo_name(build: Build, project_name: str) -> str:
    slug = "".join(c if c.isalnum() or c in "-_" else "-" for c in (project_name or "project").lower())
    slug = "-".join(filter(None, slug.split("-")))[:80] or "pmstudio-project"
    return f"{slug}-v{build.version}"


async def _get_owner(client: httpx.AsyncClient) -> str:
    _, owner = _db_creds()
    if owner:
        return owner
    r = await client.get(f"{_API}/user", headers=_headers())
    if r.status_code != 200:
        raise GitHubError(f"GitHub auth failed ({r.status_code}). Check the PAT.")
    return r.json()["login"]


async def _ensure_repo(client: httpx.AsyncClient, owner: str, name: str) -> dict[str, Any]:
    """Return the repo, creating a private auto-initialised one if missing."""
    r = await client.get(f"{_API}/repos/{owner}/{name}", headers=_headers())
    if r.status_code == 200:
        return r.json()
    # Create private repo with an initial commit so we have a base ref.
    r = await client.post(
        f"{_API}/user/repos",
        headers=_headers(),
        json={"name": name, "private": True, "auto_init": True,
              "description": "Generated by PM Studio"},
    )
    if r.status_code not in (200, 201):
        raise GitHubError(f"Could not create repo '{name}' ({r.status_code}): {r.text[:200]}")
    return r.json()


async def push_build(build_id: UUID, db: Any, project_name: str) -> dict[str, Any]:
    """Push every generated file to GitHub in one commit. Triggers CI."""
    build = db.query(Build).filter(Build.id == build_id).first()
    if not build:
        return {"error": "Build not found"}

    files = (
        db.query(GeneratedFile)
        .filter(GeneratedFile.build_id == build_id, GeneratedFile.deleted_at.is_(None))
        .all()
    )
    if not files:
        return {"error": "No files to push — generate code first."}

    async with httpx.AsyncClient(timeout=60) as client:
        owner = await _get_owner(client)
        name = build.github_full_name.split("/")[-1] if build.github_full_name else _repo_name(build, project_name)
        repo = await _ensure_repo(client, owner, name)
        full_name = repo["full_name"]
        branch = repo.get("default_branch") or "main"

        # Base ref + tree
        ref = await client.get(f"{_API}/repos/{full_name}/git/ref/heads/{branch}", headers=_headers())
        if ref.status_code != 200:
            raise GitHubError(f"Could not read base ref ({ref.status_code}): {ref.text[:200]}")
        base_sha = ref.json()["object"]["sha"]
        base_commit = await client.get(f"{_API}/repos/{full_name}/git/commits/{base_sha}", headers=_headers())
        base_tree = base_commit.json()["tree"]["sha"]

        # Blobs
        tree_items: list[dict[str, Any]] = []
        for f in files:
            blob = await client.post(
                f"{_API}/repos/{full_name}/git/blobs",
                headers=_headers(),
                json={"content": base64.b64encode(f.content.encode("utf-8", "ignore")).decode(),
                      "encoding": "base64"},
            )
            if blob.status_code not in (200, 201):
                raise GitHubError(f"Blob upload failed for {f.path} ({blob.status_code})")
            tree_items.append({"path": f.path.lstrip("/"), "mode": _GIT_MODE_FILE,
                               "type": "blob", "sha": blob.json()["sha"]})

        # Tree → commit → move ref
        tree = await client.post(
            f"{_API}/repos/{full_name}/git/trees",
            headers=_headers(),
            json={"base_tree": base_tree, "tree": tree_items},
        )
        if tree.status_code not in (200, 201):
            raise GitHubError(f"Tree creation failed ({tree.status_code}): {tree.text[:200]}")
        commit = await client.post(
            f"{_API}/repos/{full_name}/git/commits",
            headers=_headers(),
            json={"message": f"PM Studio: generated codebase ({len(files)} files)",
                  "tree": tree.json()["sha"], "parents": [base_sha]},
        )
        if commit.status_code not in (200, 201):
            raise GitHubError(f"Commit failed ({commit.status_code}): {commit.text[:200]}")
        upd = await client.patch(
            f"{_API}/repos/{full_name}/git/refs/heads/{branch}",
            headers=_headers(),
            json={"sha": commit.json()["sha"], "force": False},
        )
        if upd.status_code not in (200, 201):
            raise GitHubError(f"Ref update failed ({upd.status_code}): {upd.text[:200]}")

    build.repo_url = repo["html_url"]
    build.github_full_name = full_name
    build.default_branch = branch
    build.status = BuildStatus.qa
    db.commit()
    return {"status": "pushed", "repo_url": repo["html_url"], "full_name": full_name, "files": len(files)}


async def get_qa_status(build_id: UUID, db: Any) -> dict[str, Any]:
    """Fetch the latest GitHub Actions run and roll it into the quality report."""
    build = db.query(Build).filter(Build.id == build_id).first()
    if not build or not build.github_full_name:
        return {"error": "Build not pushed to GitHub yet"}

    async with httpx.AsyncClient(timeout=30) as client:
        r = await client.get(
            f"{_API}/repos/{build.github_full_name}/actions/runs?per_page=1",
            headers=_headers(),
        )
    if r.status_code == 401:
        return {
            "error": (
                "GitHub token is invalid or expired (401). Update it in Admin → AI config → "
                "GitHub repo token."
            ),
            "status": "auth_failed",
            "repo": build.github_full_name,
        }
    if r.status_code == 403:
        return {
            "error": (
                "GitHub token cannot read Actions runs (403). Update Admin → AI config → "
                "GitHub repo token: fine-grained PAT needs Actions → Read access on this repo."
            ),
            "status": "blocked",
            "repo": build.github_full_name,
        }
    if r.status_code != 200:
        return {
            "error": f"Could not read CI runs ({r.status_code}).",
            "status": "error",
            "repo": build.github_full_name,
        }
    runs = r.json().get("workflow_runs") or []
    if not runs:
        return {"status": "no_runs", "message": "No CI run yet — it may take a minute to start."}

    run = runs[0]
    ci_status = run.get("status")        # queued | in_progress | completed
    conclusion = run.get("conclusion")   # success | failure | cancelled | None
    report = dict(build.quality_report or {})
    report["ci"] = {
        "status": ci_status,
        "conclusion": conclusion,
        "run_url": run.get("html_url"),
        "run_id": run.get("id"),
    }
    if ci_status == "completed":
        build.quality_score = 10.0 if conclusion == "success" else 4.0
        build.status = BuildStatus.ready if conclusion == "success" else BuildStatus.failed
    build.quality_report = report
    db.commit()
    return {"status": ci_status, "conclusion": conclusion,
            "run_url": run.get("html_url"), "quality_score": build.quality_score}


_SYNC_SKIP_DIRS = {
    ".git", "node_modules", ".next", "dist", "build", "venv", ".venv",
    "__pycache__", ".turbo", ".cache", "coverage", ".pytest_cache",
}
_LANG_BY_EXT = {
    "ts": "typescript", "tsx": "typescript", "js": "javascript", "jsx": "javascript",
    "py": "python", "json": "json", "md": "markdown", "css": "css", "scss": "scss",
    "html": "html", "yml": "yaml", "yaml": "yaml", "sh": "shell", "sql": "sql",
    "toml": "toml", "ini": "ini", "env": "ini", "xml": "xml",
}


def _lang_for(path: str) -> str:
    low = path.lower()
    if "dockerfile" in low:
        return "dockerfile"
    return _LANG_BY_EXT.get(low.rsplit(".", 1)[-1], "") if "." in low else ""


async def pull_build_from_github(build_id: UUID, db: Any) -> dict[str, Any]:
    """Mirror the repo's latest commit back into the build's files (GitHub wins).

    Downloads the default-branch zipball, then for each text file: overwrites an
    existing row if changed, inserts new ones, and soft-deletes rows no longer in
    the repo — a true mirror so local edits pushed to GitHub flow back into PM Studio.
    """
    build = db.query(Build).filter(Build.id == build_id).first()
    if not build or not build.github_full_name:
        return {"error": "Build not pushed to GitHub yet"}
    branch = build.default_branch or "main"

    async with httpx.AsyncClient(timeout=120, follow_redirects=True) as client:
        r = await client.get(
            f"{_API}/repos/{build.github_full_name}/zipball/{branch}", headers=_headers()
        )
    if r.status_code == 403:
        return {"error": "GitHub token cannot read repo contents (403). Check Contents: Read access."}
    if r.status_code != 200:
        return {"error": f"Could not download repo ({r.status_code})"}

    repo_files: dict[str, str] = {}
    try:
        zf = zipfile.ZipFile(io.BytesIO(r.content))
    except Exception:  # noqa: BLE001
        return {"error": "Downloaded archive could not be read"}
    for name in zf.namelist():
        if name.endswith("/"):
            continue
        parts = name.split("/", 1)  # strip GitHub's top-level "owner-repo-sha/" folder
        if len(parts) < 2 or not parts[1]:
            continue
        rel = parts[1]
        if any(seg in _SYNC_SKIP_DIRS for seg in rel.split("/")):
            continue
        data = zf.read(name)
        if len(data) > 1_000_000:  # skip oversized/binary blobs
            continue
        try:
            repo_files[rel] = data.decode("utf-8")
        except UnicodeDecodeError:
            continue  # binary — not tracked as an editable source file

    from datetime import datetime, timezone  # noqa: PLC0415
    from app.models.build import FileStatus  # noqa: PLC0415

    existing = {
        f.path: f
        for f in db.query(GeneratedFile)
        .filter(GeneratedFile.build_id == build_id, GeneratedFile.deleted_at.is_(None))
        .all()
    }
    added = updated = removed = 0
    for path, content in repo_files.items():
        f = existing.get(path)
        if f is None:
            db.add(GeneratedFile(
                build_id=build_id, path=path, content=content,
                language=_lang_for(path), status=FileStatus.generated,
            ))
            added += 1
        elif f.content != content:
            f.content = content
            f.status = FileStatus.generated
            updated += 1
    for path, f in existing.items():
        if path not in repo_files:
            f.deleted_at = datetime.now(timezone.utc)
            removed += 1

    report = dict(build.quality_report or {})
    report["last_sync"] = {
        "at": datetime.now(timezone.utc).isoformat(),
        "added": added, "updated": updated, "removed": removed, "source": "github",
    }
    build.quality_report = report
    db.commit()
    return {"status": "synced", "added": added, "updated": updated,
            "removed": removed, "total": len(repo_files)}


async def verify_token(probe_repo: str | None = None) -> dict[str, Any]:
    """Best-effort capability check for the configured GitHub token.

    Catches the common failure modes *before* a build gets stranded:
    bad token, or a fine-grained PAT missing Actions: Read / Contents / Workflows.
    If ``probe_repo`` (an existing ``owner/name``) is given, the Actions: Read
    permission is tested for real against it.
    """
    try:
        token = _token()
    except GitHubError as exc:
        return {"ok": False, "message": str(exc), "checks": {}}

    headers = _headers()
    out: dict[str, Any] = {
        "ok": False, "login": None, "token_type": "unknown",
        "scopes": [], "checks": {}, "message": "",
    }
    async with httpx.AsyncClient(timeout=20) as client:
        u = await client.get(f"{_API}/user", headers=headers)
        if u.status_code != 200:
            out["message"] = f"Token authentication failed ({u.status_code}). Check the PAT."
            return out
        out["ok"] = True
        out["login"] = u.json().get("login")
        scopes = [s.strip() for s in u.headers.get("X-OAuth-Scopes", "").split(",") if s.strip()]
        out["scopes"] = scopes

        if token.startswith("github_pat_"):
            out["token_type"] = "fine_grained"
        elif scopes:
            out["token_type"] = "classic"

        checks: dict[str, str] = {"auth": "pass"}
        if out["token_type"] == "classic":
            # Classic PAT: 'repo' covers contents + actions read; 'workflow' for CI files.
            checks["contents"] = "pass" if "repo" in scopes else "fail"
            checks["actions_read"] = "pass" if "repo" in scopes else "fail"
            checks["workflows"] = "pass" if ("workflow" in scopes or "repo" in scopes) else "fail"
        else:
            # Fine-grained tokens don't expose scopes — verify against a repo if we can.
            checks["contents"] = "unknown"
            checks["actions_read"] = "unknown"
            checks["workflows"] = "unknown"

        if probe_repo:
            ar = await client.get(
                f"{_API}/repos/{probe_repo}/actions/runs?per_page=1", headers=headers
            )
            if ar.status_code == 200:
                checks["actions_read"] = "pass"
            elif ar.status_code == 403:
                checks["actions_read"] = "fail"
            cr = await client.get(f"{_API}/repos/{probe_repo}", headers=headers)
            if cr.status_code == 200 and checks.get("contents") == "unknown":
                checks["contents"] = "pass"

        out["checks"] = checks

    fails = [k for k, v in out["checks"].items() if v == "fail"]
    if fails:
        out["message"] = (
            "Missing permissions: " + ", ".join(fails)
            + ". A fine-grained PAT needs Contents: Read & write, Workflows: Read & write, "
            "Actions: Read."
        )
    elif any(v == "unknown" for v in out["checks"].values()):
        out["message"] = (
            f"Authenticated as {out['login']}. Fine-grained token detected — make sure "
            "Contents: Read & write, Workflows: Read & write, and Actions: Read are granted "
            "(push a build to fully verify)."
        )
    else:
        out["message"] = f"Authenticated as {out['login']}. All required permissions look good."
    return out


async def set_repo_secret(full_name: str, name: str, value: str) -> bool:
    """Set a GitHub Actions secret (libsodium sealed box). Returns False if PyNaCl
    is unavailable so the caller can fall back to manual instructions."""
    try:
        from nacl import encoding, public  # noqa: PLC0415
    except Exception:  # noqa: BLE001
        return False
    async with httpx.AsyncClient(timeout=30) as client:
        pk = await client.get(f"{_API}/repos/{full_name}/actions/secrets/public-key", headers=_headers())
        if pk.status_code != 200:
            return False
        key_b64 = pk.json()["key"]
        key_id = pk.json()["key_id"]
        sealed = public.SealedBox(public.PublicKey(key_b64.encode(), encoding.Base64Encoder))
        enc = base64.b64encode(sealed.encrypt(value.encode("utf-8"))).decode()
        r = await client.put(
            f"{_API}/repos/{full_name}/actions/secrets/{name}",
            headers=_headers(),
            json={"encrypted_value": enc, "key_id": key_id},
        )
        return r.status_code in (201, 204)


async def trigger_workflow(full_name: str, workflow_file: str, branch: str) -> bool:
    """Trigger a workflow_dispatch run for the given workflow file."""
    async with httpx.AsyncClient(timeout=30) as client:
        r = await client.post(
            f"{_API}/repos/{full_name}/actions/workflows/{workflow_file}/dispatches",
            headers=_headers(),
            json={"ref": branch},
        )
        return r.status_code in (201, 204)


def _distil_logs(raw: str, limit: int = 9000) -> str:
    """Keep the most relevant (error/fail) lines from CI logs, truncated."""
    lines = raw.splitlines()
    flagged = [ln for ln in lines if any(
        kw in ln.lower() for kw in ("error", "failed", "fail:", "✕", "exception",
                                    "cannot find", "not found", "traceback", "ts(", "eslint")
    )]
    picked = flagged if flagged else lines
    text = "\n".join(picked[-400:])
    return text[-limit:]


async def get_run_logs(full_name: str, run_id: int | str) -> str:
    """Download a completed run's logs (zip) and return distilled error text."""
    async with httpx.AsyncClient(timeout=60, follow_redirects=True) as client:
        r = await client.get(
            f"{_API}/repos/{full_name}/actions/runs/{run_id}/logs",
            headers=_headers(),
        )
    if r.status_code != 200:
        return ""
    try:
        zf = zipfile.ZipFile(io.BytesIO(r.content))
        chunks: list[str] = []
        for name in zf.namelist():
            if name.endswith(".txt"):
                chunks.append(zf.read(name).decode("utf-8", "ignore"))
        return _distil_logs("\n".join(chunks))
    except Exception:  # noqa: BLE001
        return ""
